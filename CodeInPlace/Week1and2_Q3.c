#include <stdio.h>
int main(void){
		printf("*\t*\t*\n"); // to print * * *

	    printf("**\t**\t**\n"); // to print ** ** **

	    printf("***\t***\t***"); //to print *** *** ***
	
	return 0; //tells OS: program executed successfully
}